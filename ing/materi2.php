      What are Prepositions?
A preposition is a word used to link a noun or pronoun to other words in a sentence. It shows relationships such as location, direction, time, and manner. 
Lesson: Using Prepositions in Sentences : Paragraph 1: Location The cat is on the table, sleeping peacefully. A vase with flowers is placed beside the cat. Meanwhile, the dog's leash is hanging on the wall near the door. There’s a beautiful painting above the fireplace, adding charm to the room. Can you find the book that is under the sofa?
Paragraph 2: Time and Direction We have a field trip planned on Friday. The bus will leave at 7:30 AM, so don’t be late! We will meet in front of the school. After the trip, we’ll return home by 4:00 PM. Remember to bring a snack and water for the journey. Don’t forget to pack everything in your bag the night before!
Key Points
,Prepositions of Location: on, beside, near, above, under.
,Prepositions of Time: on, at, by.
,Prepositions of Direction: in front of, for, in.
